function csl_make_slavoniser_word_map($in_text, &$w_map)
{
    if (!is_array($w_map))
        return;
    
    if (empty($in_text))
        return;
    
    $text = csl_pre_simplify_book($in_text);
    unset($in_text);
    
    $separators = csl_get_separators();
    
    $word = strtok($text, $separators);
    
    $pos = 0;

    while ($word !== false)
    {
        if ($word == '')
            continue;
        
        // get word pos here
        $pos = strpos($text, $word, $pos);
        $len = strlen($word);
        
        // only now modify Ð¹ since it's in-word and changes its length
        $word = csl_normalise($word, false); // already lowercased in pre_simplify
        
        if (!isset($w_map[$word]))
        {
            $w_map[$word] = array(csl_simplify_word($word), 1);
        }
        else
        {
            $w_map[$word][1] += 1;
        }
        
        $pos = $pos + $len;
        
        $word = strtok($separators);
    }
    
    strtok('', '');
}
    
    
function csl_make_simplified_word_map($in_text)
{
    if (empty($in_text))
        return array();
	
    $text = csl_pre_simplify_book($in_text);
    
    unset($in_text);
    
    $word_map = array();
    
    $separators = csl_get_separators();
    
    $word = strtok($text, $separators);
    
    $pos = 0;

    while ($word !== false)
    {
        if ($word == '')
            continue;
        
        // get word pos here
        $pos = strpos($text, $word, $pos);
        $len = strlen($word);
        
        // only now modify Ð¹ since it's in-word and changes its length
        $word = csl_normalise($word, false); // already lowercased in pre_simplify
        
        if (!isset($word_map[$word]))
        {
            $word_map[$word] = array(csl_simplify_word($word), $pos,  $len);
        }
        else
        {
            $word_map[$word][] = $pos;
            $word_map[$word][] = $len;
        }
        
        $pos = $pos + $len;
        
        $word = strtok($separators);
    }
    
    strtok('', '');
    
    return $word_map;
}





